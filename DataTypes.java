package classes;

public class DataTypes {

	public static void main(String[] args) {
		int a =10;
		long b = 123;
		byte c = 126;
		float d =10.2f;
		double e = 20.234567890;
		boolean f = true;
		char g = 'a';
		String h= "JAVA";
		System.out.println("DIFFERENT TYPES OF DATA TYPES : ");
		System.out.println("The integer value is : "+a);
		System.out.println("The long value is : "+b);
		System.out.println("The byte value is : "+c);
		System.out.println("The float value is : "+d);
		System.out.println("The double value is :"+e);
		System.out.println("The boolean value is : "+f);
		System.out.println("The character value is : "+g);
		System.out.println("The string value is : "+h);
		

	}

}
